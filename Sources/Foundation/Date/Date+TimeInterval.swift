import Foundation

public extension Date {
    
    /// Number of seconds per minute
    static let timeIntervalPerMinute: TimeInterval = 60.0
    
    /// Number of seconds per hour
    static let timeIntervalPerHour: TimeInterval = 3600.0
    
    /// Number of seconds per day
    static let timeIntervalPerDay: TimeInterval = 86400.0
    
    /// Number of seconds per week
    static let timeIntervalPerWeek: TimeInterval = 604800.0
    
}
